
public class Unite {
	
	protected int puissanceMax;
	protected int puissanceMin;
	protected int cout;
	protected int prioriteAtt; //priorite d'attaque
	protected int prioriteDef; //priorite defensive
	protected int mouvement; 
	
	
    public Unite(int puissanceMax, int puissanceMin, int cout, int prioriteAtt, int prioriteDef, int mouvement) {
		super();
		this.puissanceMax = puissanceMax;
		this.puissanceMin = puissanceMin;
		this.cout = cout;
		this.prioriteAtt = prioriteAtt;
		this.prioriteDef = prioriteDef;
		this.mouvement = mouvement;
	}
    
   
	
}
